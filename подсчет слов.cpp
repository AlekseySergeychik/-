#include <iostream>
#include <conio.h>
using namespace std;
enum probelilislovo{No,Yes};//no это пробел, yes это слово
int main()
{
	probelilislovo probel = No;
	char ch = 'a';
	int chisloslov = 0;
	cout << "enter text:\n";
	do
	{
		ch = _getche();
		if (ch == ' ' || ch == '\r')
		{
			if (probel == Yes)
			{
				chisloslov++;
				probel = No;
		     }

		}
		else
		{
			probel = Yes;

		};
	} while (ch != '\r');
	
	cout << "Vashe chislo slov" << chisloslov << endl;
	return 0;
	    
}

